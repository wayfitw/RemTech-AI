const Anthropic = require('@anthropic-ai/sdk');

const SYSTEM_PROMPT = `Ты — помощник по обработке коммерческих предложений на технику.
Из предоставленного текста извлеки данные и сформируй блоки для презентации.
Верни ТОЛЬКО валидный JSON без пояснений в формате:

{
  "name": "полное название техники",
  "brand": "бренд (KOMATSU / Hyundai / Caterpillar / XCMG / etc.)",
  "warranty": "гарантия",
  "availability": "наличие / срок поставки",
  "price": "цена с валютой и НДС",
  "paymentTerms": ["условие оплаты 1", "условие оплаты 2"],
  "blocks": [
    {
      "id": "block_0",
      "type": "title",
      "title": "Фронтальный погрузчик LW550RU",
      "text": "Ковш 3.5 м³, джойстик, кондиционер, удлинённая стрела"
    },
    {
      "id": "block_1",
      "type": "split",
      "title": "Технические характеристики",
      "rows": [
        ["Основные характеристики", null],
        ["Двигатель", "Cummins QSX15"],
        ["Мощность", "382 кВт / 519 л.с."],
        ["Эксплуатационная масса", "18 500 кг"],
        ["Основные размеры", null],
        ["Длина", "8 200 мм"],
        ["Ширина", "3 000 мм"],
        ["Комплектация", null],
        ["Кондиционер", ""],
        ["Джойстик", ""],
        ["Рыхлитель", ""]
      ]
    }
  ]
}

Правила формирования блоков:

1. Первый блок — ВСЕГДА type: title:
   - title = полное торговое название техники
   - text = ключевая комплектация одной строкой

2. Второй блок — ВСЕГДА type: split (фото + характеристики на одном слайде):
   - title: краткое название (например "Технические характеристики")
   - rows: массив строк вида [param, value] или [section_title, null]
   - [section_title, null] — это заголовок раздела (тёмный фон, жёлтый текст)
   - [param, value] — строка параметр=значение
   - [item, ""] — пункт комплектации (одна колонка)
   - Порядок разделов: сначала ключевые характеристики (двигатель, мощность, масса, ёмкость ковша и т.д.),
     затем основные размеры, в конце комплектация
   - Включи все важные параметры из документа: максимум 20 строк суммарно

3. Если данных очень много — добавь третий блок type: table для дополнительных характеристик

4. Цена, гарантия, наличие, условия оплаты — ТОЛЬКО в корневые поля, НЕ в блоки

5. НЕ создавать блоки для: рекламы поставщика, контактов поставщика, карты смазки, схем кабины

6. НЕ добавлять строку ["Параметр", "Значение"] как обычную строку данных`;

async function extractKPData(text) {
  const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

  const response = await client.messages.create({
    model: 'claude-sonnet-4-6',
    max_tokens: 4096,
    system: SYSTEM_PROMPT,
    messages: [{
      role: 'user',
      content: `Извлеки данные из следующего текста КП поставщика:\n\n${text}`,
    }],
  });

  const raw = response.content[0].text.trim();
  const cleaned = raw
    .replace(/^```json\s*/i, '')
    .replace(/^```\s*/i, '')
    .replace(/```\s*$/i, '')
    .trim();

  return JSON.parse(cleaned);
}

module.exports = { extractKPData };
